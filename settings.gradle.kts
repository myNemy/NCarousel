pluginManagement {
    resolutionStrategy {
        eachPlugin {
            // Some environments fail to resolve the KSP plugin marker artifact.
            // Mapping directly to the underlying module makes resolution more robust.
            if (requested.id.id == "com.google.devtools.ksp") {
                useModule("com.google.devtools.ksp:symbol-processing-gradle-plugin:${requested.version}")
            }
        }
    }
    repositories {
        google()
        mavenCentral()
        gradlePluginPortal()
    }
}

dependencyResolutionManagement {
    repositoriesMode.set(RepositoriesMode.FAIL_ON_PROJECT_REPOS)
    repositories {
        google()
        mavenCentral()
    }
}

rootProject.name = "NCarousel"
include(":app")
